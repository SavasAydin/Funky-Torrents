-module(check).
-export([gather/1]).

gather(H)->
    case H of
	H = "annouce" -> this_is_annouce;
	H = "annouce-list" -> this_is_annouce_list;
	H = "comment" -> this_is_comment;
	H = "creatin date" -> this_is_creation_date;
	H = "info" -> this_is_info;
	H = "length" -> this_is_lenght;
	H = "name" -> this_is_name;
	H = "pice lenght" -> this_is_piece_lenght;
	H = "picies" -> this_are_picies;
	H -> here_comes_the_hash
		 end.
	    
